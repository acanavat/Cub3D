static void	init_acanavat(t_data *data, char **argv);

t_data	*data_init(char **argv)
{
	t_data	*to_return;
	int		render_c;
	int		render_f;

	to_return = malloc(sizeof(t_data));
	init_acanavat(to_return, argv);
	to_return->render.image_height = DEFAULT_WINDOW_HEIGHT;
	to_return->render.image_width = DEFAULT_WINDOW_WIDTH;
	to_return->window_title = DEFAULT_WINDOW_TITLE;
	to_return->player.default_move_speed = DEFAULT_PLAYER_MOVE_SPEED;
	to_return->player.default_rot_speed = DEFAULT_PLAYER_ROT_SPEED;
	to_return->player.default_mouse_speed = DEFAULT_PLAYER_MOUSE_SPEED;
	to_return->player.default_pitch = 0;
	reset_player_to_default(&to_return->player);
	render_c = (to_return->map.ceiling_path != NULL);
	render_f = (to_return->map.floor_path != NULL);
	set_background_renderer(&to_return->raycasting, render_c, render_f);
	reset_debug_functions(to_return);
	reset_keys(&to_return->key);
	to_return->render.img = NULL;
	return (to_return);
}

void	reset_keys(t_key *key)
{
	key->rotate_up = 0;
	key->rotate_down = 0;
	key->rotate_left = 0;
	key->rotate_right = 0;
	key->move_forward = 0;
	key->move_backward = 0;
	key->move_left = 0;
	key->move_right = 0;
	key->respawn = 0;
	key->exit = 0;
	key->sprint = 0;
	key->sneak = 0;
	key->jump = 0;
}

void	reset_debug_functions(t_data *data)
{
	data->toggle_pause = 0;
	data->toggle_debug_menu = 0;
	data->toggle_key_debug_menu = 0;
	data->toggle_render_walls = 1;
	data->toggle_render_background = 1;
	data->toggle_render_crossair = 0;
	data->toggle_render_sprites = 1;
	data->last_released_key = -1;
	data->last_pressed_key = -1;
}

static void	init_acanavat(t_data *data, char **argv)
{
	data->map.stop = 0;
	data->map.last_line = -1;
	data->map.first_line = -1;
	data->map.line_size = 0;
	data->map.player_x = -1;
	data->map.player_y = -1;
	data->map.ceiling_path = NULL;
	data->map.floor_path = NULL;
	data->sprite = NULL;
	data->map.tab_map = create_map(argv[1], data);
}
